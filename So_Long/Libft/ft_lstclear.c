#include "libft.h"

void	ft_lstclear(t_list **lst, void (*del)(void*))
{
	t_list	*ptr;
	t_list	*next;

	ptr = *lst;
	while (ptr)
	{
		(*del)(ptr->content);
		next = ptr->next;
		free(ptr);
		ptr = next;
	}
	*lst = NULL;
}
